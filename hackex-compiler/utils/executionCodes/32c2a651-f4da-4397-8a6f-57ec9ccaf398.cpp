#include <iostream>

int main() {
    int num1, num2, sum;

    // Ask the user for input
    std::cin >> num1;

 
    std::cin >> num2;

    // Add the two numbers
    sum = num1 + num2;

    // Output the result
    std::cout << "The sum of " << num1 << " and " << num2 << " is " << sum << std::endl;

    return 0;
}
